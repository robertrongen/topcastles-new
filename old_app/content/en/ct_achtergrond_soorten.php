    <h3>Building structure types</h3>
	Janssen [1, p. 17] and Toy [2] both describe classifications of structure types. 
	Of these types topcastles.com deducted eight concepts that define the main groundplan of a castle.
	The concepts differ in three aspects:
<ul>	<li>the use of separate towers to defend the curtain walls;</li>
	<li>the presence of a donjon as a last defence tower;</li>
	<li>the defensive position of the donjon: inside, in or outside the curtain wall.</li>
</ul>	
	Variations and combinations of these concepts consist of extra curtain walls (Harlech Castle), donjons (Castle Duurstede) or 
	or extra baileys (Ch&acirc;teau Gaillard).
