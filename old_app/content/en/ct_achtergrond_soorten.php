    <h3>Location types</h3>
	Bodo Ebhard [3, p. 1 en 22] groups castles after the strategic advantages that the location of the castles offers.
	This location and the building materials available determines the unique character of a castle<sup>1</sup>:
	"Nach lage der Baustellen ergibt sich daher eine Unterscheidung der verschiedenen Burgenarten".
