		<h2 id="top">What is a castle?</h2>
		<h3>Definition</h3>
		This site is about "real" medieval castles, synonym for <strong>strongholds</strong>, in german 
		<strong>Burg</strong>, in french <strong>Ch&acirc;teau fort</strong> and in dutch <strong>burcht</strong>.
			
		To be able to make out the real castles we need a good definition. The book "1000 jaar kastelen in Nederland" [1] gives a useful definition of a castle:<br>
		<i>
			"A castle is a medieval building that combines defensive with domestic functions by providing a living space and a defence
			to a limited group of persons differing of a noble family with a few servants to a small court and a military garrison up to a maximum of 
			approximately fifty persons. Essential hereby is that this group depends on a relation to one person or institution."  
		</i>
		Summarised, a real castle meets these criteria:<br>
		<ul>
			<li>it is build in the middle ages (appr. 500-1500) </li>
			<li>it provides a living space  to a limited group of persons</li>
			<li>it provides a defence to a limited group of persons </li>
			<li>the group depends on a relation to one person or institution</li>
		</ul>	<h3>City castles</h3>
		A special case is the city castle. When is such a castle qualified as castle and when as an walled city?  
		Sir Oman [2] gives a useful definition of a city castle: <br>
		<i>
			"A castle is a military structure larger than a tower but smaller than a fortified town,
			it is residential but is also defensible in character.
			However complicated internally it must be a complete unit unto itself and not part of a town.
			A castle may exist inside a town but must be able to be cut off by the closing of a gate
			or the raising of a drawbridge."
		</i>
		
		To be qualified as a castle, a city castle must have be able defend itself independable of the city and the city walls. Two famous examples of such a city castle are <a title="Caernarfon Castle (Wales)" href="javascript:document.form81.SelCastle.value='caernarfon';document.form81.submit();">Caernarfon Castle</a> and <a title="Carcassonne (France)" href="javascript:document.form81.SelCastle.value='carcassonne';document.form81.submit();">Ch&acirc;teau de Carcassonne</a>.
