	  	<h2 id="top">Which castles do not qualify?</h2>
	  	<h3>Categories</h3>
		Some famous castles do not qualify as castle following the definition. 
		In order to illustrate this four kinds of categories of castles that do not meet the definition can be defined:
		<ul>	
			<li>Renaissance-, romantic- and new castles</li>
			<li>Emperial, royal and noble palaces</li>
			<li>Fortifications</li>
			<li>Walled cities</li>
		</ul>	
	</td>
</tr>
