	    <h3>Emperial, royal and noble palaces</h3>
		The emperors of the holy roman empire but also kings and other noblemen build palaces (&quot;Pfalzen&quot;) that had residential, adminstrative or representative function, but did not have a defensive function. </p>
		Examples:
		<ul>	<li>The Ridderzaal of the Binnenhof in the Hague (Netherlands)</li>
			<li>Pfalz Goslar (Germany)</li>
			<li>Westminster Hall (England)</li>
		</ul>	
