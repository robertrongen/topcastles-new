  	<h2 id="top">Calculation of the top 100 scores</h2>
	<br>Topcastles.com uses two ratings to calculate the top 100 score:
<ul>	<li>the appearance of the castle in specific sources</li>
	<li>the rating of the castle given by visitors of topcastles.com</li>
</ul>	
	The final score of a castle is the sum of the sources score plus the visitors rating. 
