  	<h3 id="top">Calculation of the score for the Visitors Top 100</h3>
	Each castle gets an initial score of 5. Upon the first score the average rating is calculated.
	A first score of 10 results than in a rating of (5+10)/2 = 7.5. 
	The average score is recalculated automatically on every vote.
  	<h3 id="top">Calculation of the total score</h3>
	In January 1st of each year the visitor rating of a castle is added to the sources code to calculate the new top 100 for that year.
	Relatively in the end score, the sources score weighs twice as much as the visitors rating. 
