  	<h2 id="top">Attempt to create a neutral rating for a castle top 100</h2>
	<br>What makes a castle a Topcastle? 
	<br><br>
	First: The "charisma" of a castle, a combination of its strategic location, through which the castle appears to dominate the landscape, 
	the design that pairs beauty to an awe inspiring deterrence and the used materials through which the castle appears to dissolve its surroundings.  
	Ditches with drwbridges, towers with battlements, halls with crosses and swords and dungeons and secret courses stand symbol for jurisdiction and protection only also for oppression and cruelty.  
	A topkasteel recalls memories of sieges and conquests, famous inhabitants and prisoners, destruction and reconstruction and affirms its myths and ghost stories.  	
	<br><br>
	The proposition of topcastles.com is that the extent in which a castle meets this description is proportional to the attention that castle gets in  books about castles and on the internet.
	A beautiful castle, a turbulent history or a remarkable military architecture inspires authors and photographers.
	In a nutshell, a topcastle can measured by the amount of stories and photographs it appears in.  	
	The authors and editors of books and websites make a virtual jury through their selection of castles, photographs and lay the basis for this castle top 100.  
