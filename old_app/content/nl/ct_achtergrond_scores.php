  	<h2 id="top">Berekening van de top 100 score</h2>
	<br>De top 100 gebruikt twee oordelen om de scores voor de top 100 te bepalen:
<ul>	<li>het oordeel van bronnen</li>
	<li>het oordeel van bezoekers van topkastelen.nl</li>
</ul>	
	De score in de top 100 lijst is de som van de bronnenscore plus het oordeel van de bezoekers. 
