  	<h3 id="top">Berekening van de score van bezoekers top 100</h3>
	Ieder kasteel krijgt een basisscore van 5. 
	Als de eerste stem is uitgebracht wordt automatisch de gemiddelde score berekend.
	Een eerste score van 10 resulteert dan in (5+10)/2 = 7.5. 
	Bij elke volgende score wordt opnieuw het gemiddelde uitgerekend.
  	<h3 id="top">Berekening van de totaalscore</h3>
	Elk jaar wordt op op 1 januari het bezoekersoordeel van dat moment opgeteld bij de bronnenscore.
	De bronnenscore telt in verhouding tweemaal mee en de bezoekersscore eenmaal. 
