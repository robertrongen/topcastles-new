  	<h2 id="top">Poging tot een objectieve beoordeling van kastelen</h2>
	<br>Wat maakt een kasteel een topkasteel? 
	<br><br>
	Allereerst: De uitstraling van een kasteel, een combinatie van zijn strategische ligging waardoor het kasteel het landschap lijkt te domineren, de vormgeving die schoonheid paart aan een ontzag inboezemende afschrikking en de gebruikte materialen waardoor het kasteel lijkt te versmelten met zijn omgeving. 
	Grachten met ophaalbruggen, torens met kantelen, zalen met kruis en zwaard en kerkers en geheime gangen staan symbool voor rechtspraak en bescherming maar ook voor onderdrukking en wreedheid. 
	Een topkasteel roept beelden op van een historie van belegeringen en veroveringen, beroemde bewoners en gevangenen, verwoesting en wederopbouw en bevestigt de mythes en de spookverhalen.
	<br><br>
	De stelling van topkastelen.nl is dat de mate waarin een kasteel voldoet aan deze beschrijving evenredig is met de aandacht die in boeken en op internet wordt gegeven aan dat kasteel. 
	Van een mooi kasteel worden veel foto's gemaakt, over een turbulente geschiedenis of een opmerkelijke militaire architectuur wordt geschreven. Plat gezegd: Over een topkasteel verschijnen veel verhalen en foto's. 
	De auteurs en redacteuren van boeken en websites over kastelen vormen zo een onzichtbare jury die door hun selectie van kastelen, foto's en verhalen de basis leggen voor een kastelen top 100.
