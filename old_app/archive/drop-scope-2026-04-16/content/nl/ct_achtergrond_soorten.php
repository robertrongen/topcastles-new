    <h3>Naar bouwconcept</h3>
	Uit indelingen die Janssen [1, p. 17] en Toy [2] gebruiken kan je acht concepten afleiden waarop het grondplan van een kasteel is gebaseerd.
	De concepten verschillen van elkaar op drie punten:
<ul>	<li>het gebruik van aparte torens voor de verdediging van de weermuren;</li>
	<li>de aanwezigheid van een donjon als laatste verdedingingsplaats;</li>
	<li>de positie van de donjon ten opzichte van de weermuren: erbinnen, erbuiten of erin.</li>
</ul>	
	Binnen een concept zijn nog varianten mogelijk zoals een extra weermuur om het kasteel heen (zoals bij Harlech Castle) 
	of meerdere voorburchten (zoals bij Ch&acirc;teau Gaillard).
