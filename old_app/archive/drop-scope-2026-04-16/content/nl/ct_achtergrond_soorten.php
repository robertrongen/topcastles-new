    <h3>Naar ligging</h3>
	Volgens Bodo Ebhard [3, p. 1 en 22] werd de ligging van een kasteel geheel bepaald door de verdedigbaarheid van die plaats.
	Deze plaats bepaald dan tevens de bouwmogelijkheden en levert meestal ook de bouwmaterialen<sup>1</sup>:
	"Nach lage der Baustellen ergibt sich daher eine Unterscheidung der verschiedenen Burgenarten".
