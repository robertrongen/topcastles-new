    <h3>Renaissance-, romantische- en nieuwe kastelen</h3>
	Na de uitvinding van het buskruid in de 15de eeuw verloren de kastelen hun verdedigbare karakter. 
	In de plaats van kastelen verschenen forten en renaissance kastelen. 
	Renaissance kastelen lijken van buiten nog vaak op middeleeuwse kastelen, 
	maar zijn niet gebouwd om verdedigd te kunnen. Het zijn in feite comfortablele landhuizen of paleizen die de krachtige uitstraling hebben van middeleeuwse kastelen.
<br><br>
	Voorbeelden van renaissance kastelen:
<ul>	<li>Ch&acirc;teau de Chambord (Frankrijk)</li>
	<li>Ch&acirc;teau de Chenonceaux (Frankrijk)</li>
	<li>Ch&acirc;teau d'Uss&eacute; (Frankrijk)</li>
</ul>	

Romantische kastelen zijn in de 19de eeuw gebouwd en weerspiegelen het terugverlangen naar een
	veridealiseerd beeld van de middeleeuwen. 	
<br><br>
	Voorbeelden van romantische kastelen:
<ul><li>Schlo&szlig; Neuschwanstein (Duitsland)</li>
	<li>Schlo&szlig; Liechtenstein (Duitsland)</li>
	<li>Paleis van Willem II in Tilburg (Nederland)</li>
</ul>	

	In de twintigste eeuw kwam er meer aandacht voor het werkelijke karakter
	van middeleeuwse kastelen en werd getracht tijdens restauraties en nieuwbouw zo veel mogelijk de middeleeuwse werkelijkheid te weerspiegelen.
<br><br>
	Voorbeelden van nieuwe kastelen:
	
<ul>	<li>Kasteel van Almere (Nederland)</li>
</ul>  	</td></tr>
