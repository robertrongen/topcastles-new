    <h3>Definitie</h3>
	Deze site gaat alleen over "echte" middeleeuwse kastelen, ook wel aangeduid met <strong>burchten</strong>, 
	of in het duits <strong>Burg</strong>, in het engels <strong>stronghold</strong> of in het franse <strong>Ch&acirc;teau fort</strong>.

	In het boek "1000 jaar kastelen in Nederland" [1] staat op pagina 15 een bruikbare definitie:<br>
	<br><i>"een kasteel is een middeleeuws gebouw dat de functies van verdedigbaarheid en bewoonbaarheid combineert,
	door het verschaffen van woonruimte en verdedigbaarheid aan aan beperkte groep mensen varierend van een adellijke familie
	met een paar dienaren tot een kleine hofhouding en een militair garnizoen tot een maximum van ongeveer vijftig personen.
	Essentieel hierbij is dat deze groep mensen in een afhankelijkheidsrelatie staat tot &eacute;&eacute;n persoon of instelling."
	</i>
<br><br>
	Samengevat voldoet een kasteel aan de volgende eisen:<br>
<ul>	<li>gebouwd in de middeleeuwen</li>
	<li>bewoond door een groep mensen (max. 50)</li>
	<li>verdedigbaar</li>
	<li>de groep mensen staat in een afhankelijkheidsrelatie tot &eacute;&eacute;n persoon of instelling</li>
</ul>	

    <h3>Stadsburchten</h3>
	Een bijzonder geval is de stadsburcht. Wanneer is er sprake van een kasteel en wanneer van een ommuurde stad? 
	Sir Oman [2] geeft een bruikbare definitie van een stadsburcht:
<br><br>
	<i>"A castle is a military structure larger than a tower but smaller than a fortified town,
	it is residential but is also defensible in character.
	However complicated internally it must be a complete unit unto itself and not part of a town.
	A castle may exist inside a town but must be able to be cut off by the closing of a gate
	or the raising of a drawbridge."
	</i>
<br><br>
	Bij een stadsburcht moet het kasteelgedeelte dus af te sluiten zijn van de stad en de stadsmuur. 
	Twee bekende voorbeelden van stadsburchten die van de ommuurde stad zijn af te sluiten zijn
